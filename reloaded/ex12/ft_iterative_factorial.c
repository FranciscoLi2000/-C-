#include <unistd.h>

int	ft_iterative_factorial(int nb)
{
	int	i;
	int	result;

	if (nb < 0)
		return (0);
	i = 1;
	result = 1;
	while (i <= nb)
	{
		if (result > 2147483647 / i)
			return (0);
		result = result * i;
		i++;
	}
	return (result);
}

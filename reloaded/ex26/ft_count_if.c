#include <unistd.h>

int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;

	if (!tab || !f)
		return (0);
	i = 0;
	while (*tab != NULL)
	{
		if (f(*tab) == 1)
			i++;
		tab++;
	}
	return (i);
}
